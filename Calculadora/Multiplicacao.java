package Calculadora;

/**
 * @author: Gabriel Arruda
 * @version: 1.0
 * @since: Release 1.0 da aplicação
 *
 * Classe que implementa a funcionalidade de multiplicação de dois números inteiros.
 */
public class Multiplicacao {

    /**
     * Método estático para multiplicar dois números inteiros.
     *
     * @param a O primeiro número inteiro a ser multiplicado.
     * @param b O segundo número inteiro a ser multiplicado.
     * @return O resultado da multiplicação entre os dois números.
     */
    public static int multiplicar(int a, int b) {
        return a * b;
    }
}
