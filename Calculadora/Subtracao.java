package Calculadora;

/**
 * @author: Gabriel Arruda
 * @version: 1.0
 * @since: Release 1.0 da aplicação
 *
 * Classe que implementa a funcionalidade de subtração de dois números inteiros.
 */
public class Subtracao {

    /**
     * Método estático para subtrair dois números inteiros.
     *
     * @param a O número inteiro do qual será subtraído o segundo número.
     * @param b O número inteiro a ser subtraído do primeiro número.
     * @return O resultado da subtração entre os dois números.
     */
    public static int subtrair(int a, int b) {
        return a - b;
    }
}
